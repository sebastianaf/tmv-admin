import express from "express";

import ProjectFileService from "../services/projectFile.service";
import {
  getIdProjectFileSchema,
  getIdQueryProjectFileSchema,
  postProjectFileSchema,
  patchProjectFileSchema,
} from "../schemas/projectFile.schema";
import validatorHandler from "../middlewares/validator.handler";

const router = express.Router();
const service = new ProjectFileService();

router.get(
  "/",
  validatorHandler(getIdQueryProjectFileSchema, `query`),
  async (req, res, next) => {
    try {
      let result = [];
      if (req.query._id) {
        result = await service.findOne(req.query._id);
      } else {
        result = await service.find({});
      }
      res.status(200).json({ statusCode: 200, error: null, data: result });
      next();
    } catch (error) {
      next(error);
    }
  }
);

router.post(
  "/",
  validatorHandler(postProjectFileSchema, `body`),
  async (req, res, next) => {
    try {
      const result = await service.create(req.body);
      res.status(200).json({ statusCode: 200, error: null, data: result });
      next();
    } catch (error) {
      next(error);
    }
  }
);

router.patch(
  "/",
  validatorHandler(getIdProjectFileSchema, `query`),
  validatorHandler(patchProjectFileSchema, `body`),
  async (req, res, next) => {
    try {
      const result = await service.update(req.query._id, req.body);
      res.status(200).json({ statusCode: 200, error: null, data: result });
      next();
    } catch (error) {
      next(error);
    }
  }
);

router.delete(
  "/",
  validatorHandler(getIdProjectFileSchema, `query`),
  async (req, res, next) => {
    try {
      const result = await service.delete(req.query._id);
      res.status(200).json({ statusCode: 200, error: null, data: result });
      next();
    } catch (error) {
      next(error);
    }
  }
);

export default router;
