import express from "express";

import EmployeeTypeService from "../services/employeeType.service";
import {
  getIdEmployeeTypeSchema,
  getIdQueryEmployeeTypeSchema,
  postEmployeeTypeSchema,
  patchEmployeeTypeSchema,
} from "../schemas/employeeType.schema";
import validatorHandler from "../middlewares/validator.handler";

const router = express.Router();
const service = new EmployeeTypeService();

router.get(
  "/",
  validatorHandler(getIdQueryEmployeeTypeSchema, `query`),
  async (req, res, next) => {
    try {
      let result = [];
      if (req.query._id) {
        result = await service.findOne(req.query._id);
      } else {
        result = await service.find({});
      }
      res.status(200).json({ statusCode: 200, error: null, data: result });
      next();
    } catch (error) {
      next(error);
    }
  }
);

router.post(
  "/",
  validatorHandler(postEmployeeTypeSchema, `body`),
  async (req, res, next) => {
    try {
      const result = await service.create(req.body);
      res.status(200).json({ statusCode: 200, error: null, data: result });
      next();
    } catch (error) {
      next(error);
    }
  }
);

router.patch(
  "/",
  validatorHandler(getIdEmployeeTypeSchema, `query`),
  validatorHandler(patchEmployeeTypeSchema, `body`),
  async (req, res, next) => {
    try {
      const result = await service.update(req.query._id, req.body);
      res.status(200).json({ statusCode: 200, error: null, data: result });
      next();
    } catch (error) {
      next(error);
    }
  }
);

router.delete(
  "/",
  validatorHandler(getIdEmployeeTypeSchema, `query`),
  async (req, res, next) => {
    try {
      const result = await service.delete(req.query._id);
      res.status(200).json({ statusCode: 200, error: null, data: result });
      next();
    } catch (error) {
      next(error);
    }
  }
);

export default router;
